# 글자수

t = int(input())
for tc in range(1, t+1):
    word_key = list(map(str, input()))
    word_lock = list(map(str, input()))


    answer_list = []
    for i in range(len(word_key)):
        for j in range(1, len(word_key)):
            for k in range(len(word_lock)):
                if word_key[i] == word_key[j]:
                    continue
                if word_key[i] == word_lock[k]:
                    answer_list.append(word_key[i])



    print(answer_list)


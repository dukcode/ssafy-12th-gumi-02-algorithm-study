# 입력값
# 1
# 2
# 1줄로 받는 것이 아니라 2줄로 받음.


# input 받는 값의 줄이 다름으로 나눠서 표기해야함.
# 1번을 정수로 받는 a
a = int(input())
# 2번을 정수로 받는 b
b = int(input())

# a 와 b의 합을 출력
print(a+b)

# 이후 다른 줄에 있는 1과 2를 터미널에 입력하면 정상적으로 값이 출력됨.


# 굳이 이 쉬운 문제를 풀이한 이유는 터미널에 input 값을 입력할때 다른줄에 있다면
# 변수 역시 줄에 맞게 받아야한다는 사실을 몰라 30분을 소요한 기억때문에 풀이함.